-- Create tables without foreign key constraints first
CREATE TABLE CUSTOMER (
    CUSTOMER_ID NUMBER PRIMARY KEY,
    FIRST_NAME VARCHAR2(50),
    SURNAME VARCHAR2(50),
    ADDRESS VARCHAR2(100),
    CONTACT_NUMBER VARCHAR2(15),
    EMAIL VARCHAR2(100)
);

CREATE TABLE EMPLOYEE (
    EMPLOYEE_ID VARCHAR2(10) PRIMARY KEY,
    FIRST_NAME VARCHAR2(50),
    SURNAME VARCHAR2(50),
    CONTACT_NUMBER VARCHAR2(15),
    ADDRESS VARCHAR2(100),
    EMAIL VARCHAR2(100)
);

CREATE TABLE DONATOR (
    DONATOR_ID NUMBER PRIMARY KEY,
    FIRST_NAME VARCHAR2(50),
    SURNAME VARCHAR2(50),
    CONTACT_NUMBER VARCHAR2(15),
    EMAIL VARCHAR2(100)
);

CREATE TABLE DONATION (
    DONATION_ID NUMBER PRIMARY KEY,
    DONATOR_ID NUMBER,
    DONATION VARCHAR2(100),
    PRICE NUMBER(10, 2),
    DONATION_DATE DATE
);

CREATE TABLE DELIVERY (
    DELIVERY_ID NUMBER PRIMARY KEY,
    DELIVERY_NOTES VARCHAR2(200),
    DISPATCH_DATE DATE,
    DELIVERY_DATE DATE
);

CREATE TABLE RETURNS (
    RETURN_ID VARCHAR2(10) PRIMARY KEY,
    RETURN_DATE DATE,
    REASON VARCHAR2(100),
    CUSTOMER_ID NUMBER,
    DONATION_ID NUMBER,
    EMPLOYEE_ID VARCHAR2(10)
);

CREATE TABLE INVOICE (
    INVOICE_NUM NUMBER PRIMARY KEY,
    CUSTOMER_ID NUMBER,
    INVOICE_DATE DATE,
    EMPLOYEE_ID VARCHAR2(10),
    DONATION_ID NUMBER,
    DELIVERY_ID NUMBER
);

-- Add foreign key constraints after all tables are created
ALTER TABLE DONATION ADD CONSTRAINT fk_donator
    FOREIGN KEY (DONATOR_ID) REFERENCES DONATOR(DONATOR_ID);

ALTER TABLE RETURNS ADD CONSTRAINT fk_returns_customer
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID);

ALTER TABLE RETURNS ADD CONSTRAINT fk_returns_donation
    FOREIGN KEY (DONATION_ID) REFERENCES DONATION(DONATION_ID);

ALTER TABLE RETURNS ADD CONSTRAINT fk_returns_employee
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(EMPLOYEE_ID);

ALTER TABLE INVOICE ADD CONSTRAINT fk_invoice_customer
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID);

ALTER TABLE INVOICE ADD CONSTRAINT fk_invoice_employee
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(EMPLOYEE_ID);

ALTER TABLE INVOICE ADD CONSTRAINT fk_invoice_donation
    FOREIGN KEY (DONATION_ID) REFERENCES DONATION(DONATION_ID);

ALTER TABLE INVOICE ADD CONSTRAINT fk_invoice_delivery
    FOREIGN KEY (DELIVERY_ID) REFERENCES DELIVERY(DELIVERY_ID);

